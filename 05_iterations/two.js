// mostly we use "for" loop.

// while loop

// simple structure of while loop
// while (condition) {
    
// }

let index = 0
while (index <= 10) {
    // console.log(`Value of index is ${index}`);
    index = index + 2
}

let myArray = ['flash', 'batman', 'superman']
let arr = 0
while (arr < myArray.length) {
    // console.log(`value is ${myArray[arr]}`);
    arr = arr + 1
}

// do while loop

// simple structure of do while loop
// do {
    
// } while (condition);

let score = 1

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);